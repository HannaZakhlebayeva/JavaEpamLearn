package by.roger.epamLearn.moduleOne;

public class Dot {
    private final double x;
    private final double y;

    public Dot(double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
